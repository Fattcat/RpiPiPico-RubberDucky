def text_to_hid_array(text: str):
    arr = []
    for c in text:
        if c in char_map:
            keycode, mod = char_map[c]
            arr.append(mod)
            arr.append(keycode)
        else:
            print(f"Warning: znak '{c}' nie je v mape, nahradzujem medzerou")
            arr.append(0x00)
            arr.append(0x2C)  # space
    return arr

def format_cpp_array(arr):
    lines = []
    line = ""
    for i, v in enumerate(arr):
        line += f"0x{v:02X}, "
        if (i + 1) % 16 == 0:
            lines.append(line)
            line = ""
    if line:
        lines.append(line)
    return "\n".join(lines)

def main():
    vstup = input("Zadaj text na konverziu do HID kódov (SK layout):\n")
    arr = text_to_hid_array(vstup)
    print("\n// Vygenerované HID pole pre SK layout\n")
    print("const uint8_t key_arr[] PROGMEM = {")
    print(format_cpp_array(arr))
    print("};")

if __name__ == "__main__":
    main()
